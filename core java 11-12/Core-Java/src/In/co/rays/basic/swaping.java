package In.co.rays.basic;

public class swaping {
public static void main (String[]args) {
	int a=25;
	int b=35;
	int c=0;
	System.out.println("a= "   +a);
	System.out.println("b = "   +b);
	c=a; // c=25;
	a=b; // a=35;
	b=c; // b=25;
	System.out.println("---------");
	
	System.out.println("a= "  +a);
	System.out.println("b=  "  +b);
	
}
}
